    function resolveContactAssocLabel(type, value) {
        const raw = String(value == null ? '' : value).trim();
        if (!raw || /^none$/i.test(raw)) return 'None';
        const optionsState = window.__miniContactAssocOptions || {};
        const options = Array.isArray(optionsState[type]) ? optionsState[type] : [];
        if (type === 'worldbook') {
            const labels = parseWorldbookAssocValues(raw)
                .map((item) => {
                    const match = options.find((option) => option && option.value === item);
                    return match && match.label ? match.label : item.replace(/^worldbook:/i, 'Group ');
                })
                .filter(Boolean);
            if (!labels.length) return 'None';
            if (labels.length === 1) return labels[0];
            if (labels.length === 2) return labels.join(' / ');
            return `${labels.slice(0, 2).join(' / ')} +${labels.length - 2}`;
        }
        const match = options.find((item) => item && item.value === raw);
        if (match && match.label) return match.label;
        const legacyMap = type === 'preset'
            ? { MAIN_CHAR: 'Main Character', SIDE_KICK: 'Side Kick', VILLAIN: 'Villain' }
            : { GLOBAL_RULE: 'Global Rules', CITY_LORE: 'City Lore' };
        return legacyMap[raw] || raw;
    }

