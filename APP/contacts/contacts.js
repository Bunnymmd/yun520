    function normalizeWorldbookAssocToken(value) {
        return String(value == null ? '' : value).trim();
    }

    function parseWorldbookAssocValues(value) {
        const raw = String(value == null ? '' : value).trim();
        if (!raw || /^none$/i.test(raw)) return [];
        return [...new Set(
            raw
                .split(/[|,\n]/)
                .map((item) => normalizeWorldbookAssocToken(item))
                .filter((item) => item && !/^none$/i.test(item) && /^worldbook:/i.test(item))
        )];
    }

    function serializeWorldbookAssocValues(values) {
        const normalized = [...new Set((Array.isArray(values) ? values : [])
            .map((item) => normalizeWorldbookAssocToken(item))
            .filter((item) => item && /^worldbook:/i.test(item)))];
        return normalized.length ? normalized.join('|') : 'NONE';
    }

    function mergeWorldbookAssocValues(currentValue, incomingValue) {
        return serializeWorldbookAssocValues([
            ...parseWorldbookAssocValues(currentValue),
            ...parseWorldbookAssocValues(incomingValue)
        ]);
    }

