    async function saveContact() {
        if (contactSavePending) return;
        const contactData = collectCurrentContactFormData();

        if(!contactData.nickname) return alert("Nickname is required. (昵称为必填项)");

        contactSavePending = true;
        setPrimaryContactActionPending(true);
        try {
            let savedContactId = null;
            const duplicate = await findDuplicateContact(contactData, currentEditId);
            if (duplicate && currentEditId && duplicate.id !== currentEditId) {
                alert("检测到重复联系人，请直接编辑现有条目。");
                return;
            }

            if (duplicate && !currentEditId) {
                const changes = buildMergedContactChanges(duplicate, contactData);
                if (Object.keys(changes).length) {
                    await db.contacts.update(duplicate.id, changes);
                }
                savedContactId = duplicate.id;
                alert("检测到重复联系人，已自动合并到现有条目。");
            } else if (currentEditId) {
                await db.contacts.update(currentEditId, contactData);
                savedContactId = currentEditId;
            } else {
                savedContactId = await db.contacts.add(contactData);
            }
            closeEditor();
            void maybeGenerateContactPersona(savedContactId, { force: true, silent: true });
        } finally {
            contactSavePending = false;
            setPrimaryContactActionPending(false);
        }
    }

