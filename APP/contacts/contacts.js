    async function importData(event) {
        if (contactImportPending) return;
        const file = event.target.files[0];
        if (!file) return;
        contactImportPending = true;

        try {
            let contactData = null;
            let avatarBase64 = null;

            if (file.name.toLowerCase().endsWith('.json')) {
                const text = await file.text();
                contactData = JSON.parse(text);
            } 
            else if (file.name.toLowerCase().endsWith('.png')) {
                avatarBase64 = await new Promise((res) => {
                    const r = new FileReader();
                    r.onload = (e) => res(e.target.result);
                    r.readAsDataURL(file);
                });
                if (!window.MiniPngCard || typeof window.MiniPngCard.readFile !== 'function') {
                    throw new Error("PNG import helper is not available.");
                }
                const embeddedPayload = await window.MiniPngCard.readFile(file);
                if (embeddedPayload.mini && embeddedPayload.mini.app === 'mini' && embeddedPayload.mini.data) {
                    contactData = buildImportedContactFromCharacterData(embeddedPayload.mini.data, avatarBase64);
                    if (embeddedPayload.mini.format === 'contact' && Array.isArray(embeddedPayload.mini.data.relations)) {
                        contactData.relations = embeddedPayload.mini.data.relations;
                    }
                } else if (embeddedPayload.chara) {
                    contactData = buildImportedContactFromCharacterData(embeddedPayload.chara.data || embeddedPayload.chara, avatarBase64);
                } else {
                    throw new Error("No embedded character data found in PNG.");
                }
            }

            if (contactData && contactData.nickname) {
                const importedRelations = contactData.relations;
                const originalId = contactData.id;
                const originalType = contactData.type || currentListType;

                delete contactData.id; 
                delete contactData.relations;
                contactData.type = currentListType; 
                if(!contactData.account) contactData.account = generateUniqueAccount();
                if(!contactData.avatar && avatarBase64) contactData.avatar = avatarBase64;
                if(!contactData.language) contactData.language = '中文 (Mandarin)';

                const duplicate = await findDuplicateContact(contactData);
                let targetId = null;
                let merged = false;

                if (duplicate) {
                    const changes = buildMergedContactChanges(duplicate, contactData);
                    if (Object.keys(changes).length) {
                        await db.contacts.update(duplicate.id, changes);
                    }
                    targetId = duplicate.id;
                    merged = true;
                } else {
                    targetId = await db.contacts.add(contactData);
                }

                const newGlobalId = currentListType + '_' + targetId;
                const oldGlobalId = originalId != null ? `${originalType}_${originalId}` : '';
                await restoreImportedRelations(importedRelations, oldGlobalId, newGlobalId);

                loadList();
                void maybeGenerateContactPersona(targetId, { force: true, silent: true });
                alert(merged ? '检测到重复联系人，已导入并合并到现有条目。' : '导入成功 (Import successful)');
            } else {
                throw new Error("Format not recognized.");
            }
        } catch(err) {
            console.error(err);
            alert('导入失败 (Import failed): ' + err.message);
        } finally {
            contactImportPending = false;
            event.target.value = '';
        }
    }

