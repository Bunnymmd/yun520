    async function saveRelation() {
        const relEditId = document.getElementById('inp-rel-edit-id').value;
        const targetGlobalId = document.getElementById('inp-rel-target').value;
        let relType = document.getElementById('inp-rel-type').value;
        
        if(relType === 'CUSTOM') {
            relType = document.getElementById('inp-rel-custom').value.trim();
            if(!relType) return alert("Please specify the custom relation. (请填写自定义关系)");
        } else {
            relType = normalizeRelationType(relType);
        }
        
        if(!targetGlobalId) return alert("Please select a target entity. (请选择目标)");

        const relDesc = document.getElementById('inp-rel-desc').value.trim();
        if(!relDesc) return alert("Please describe the relation details. (请填写关系描述)");
        
        const relData = {
            sourceGlobalId: currentNetworkEntity.globalId,
            targetGlobalId: targetGlobalId,
            relationType: relType,
            relationDesc: relDesc
        };

        if (relEditId) {
            await db.relations.update(parseInt(relEditId), relData);
        } else {
            await db.relations.add(relData);
        }
        
        closeRelationModal();
        loadRelations();
    }

