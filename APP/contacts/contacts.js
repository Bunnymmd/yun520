    function normalizeContactIdentityText(value) {
        return String(value == null ? '' : value)
            .replace(/\r/g, ' ')
            .replace(/\s+/g, ' ')
            .trim()
            .toLowerCase();
    }

    function isAutoGeneratedAccount(value) {
        return /^[CN]_[A-Z0-9]+_[A-Z0-9]{4}$/i.test(String(value || '').trim());
    }

    function buildContactDuplicateFingerprint(contact) {
        if (!contact || typeof contact !== 'object') return '';
        const type = normalizeContactIdentityText(contact.type) || 'char';
        const account = normalizeContactIdentityText(contact.account);
        if (account && !isAutoGeneratedAccount(account)) return `account:${type}:${account}`;

        const nickname = normalizeContactIdentityText(contact.nickname);
        const name = normalizeContactIdentityText(contact.name);
        const signature = normalizeContactIdentityText(contact.signature);
        const lore = normalizeContactIdentityText(contact.lore);
        const avatar = String(contact.avatar || '').trim();
        const identity = [nickname, name].filter(Boolean).join('|');
        const narrative = [signature, lore].filter(Boolean).join('|');

        if (!identity) return '';
        if (narrative && narrative.length >= 16) return `story:${type}:${identity}:${narrative}`;
        if (avatar && narrative) return `portrait:${type}:${identity}:${narrative}:${avatar.slice(0, 96)}`;
        if (avatar && nickname && name) return `portrait:${type}:${nickname}|${name}:${avatar.slice(0, 96)}`;
        return '';
    }

    function pickPreferredContactValue(currentValue, incomingValue, options = {}) {
        const currentText = String(currentValue == null ? '' : currentValue).trim();
        const incomingText = String(incomingValue == null ? '' : incomingValue).trim();
        if (!incomingText) return currentText;
        if (!currentText) return incomingText;
        if (options.preferIncomingWhenLonger && incomingText.length > currentText.length + 8) return incomingText;
        return currentText;
    }

    function buildMergedContactChanges(existing, incoming) {
        if (!existing || !incoming) return {};
        const next = {
            nickname: pickPreferredContactValue(existing.nickname, incoming.nickname, { preferIncomingWhenLonger: true }),
            name: pickPreferredContactValue(existing.name, incoming.name, { preferIncomingWhenLonger: true }),
            gender: (existing.gender && existing.gender !== 'X') ? existing.gender : (incoming.gender || existing.gender || 'X'),
            account: (!isAutoGeneratedAccount(existing.account) && String(existing.account || '').trim())
                ? existing.account
                : pickPreferredContactValue(existing.account, incoming.account),
            language: pickPreferredContactValue(existing.language, incoming.language),
            signature: pickPreferredContactValue(existing.signature, incoming.signature, { preferIncomingWhenLonger: true }),
            lore: pickPreferredContactValue(existing.lore, incoming.lore, { preferIncomingWhenLonger: true }),
            presetAssoc: (existing.presetAssoc && existing.presetAssoc !== 'NONE') ? existing.presetAssoc : (incoming.presetAssoc || 'NONE'),
            worldbookAssoc: mergeWorldbookAssocValues(existing.worldbookAssoc, incoming.worldbookAssoc),
            avatar: pickPreferredContactValue(existing.avatar, incoming.avatar)
        };
        const changes = {};
        Object.keys(next).forEach((key) => {
            const currentText = String(existing[key] == null ? '' : existing[key]).trim();
            const nextText = String(next[key] == null ? '' : next[key]).trim();
            if (currentText !== nextText) changes[key] = next[key];
        });
        return changes;
    }

    async function findDuplicateContact(contactData, excludeId = null) {
        const fingerprint = buildContactDuplicateFingerprint(contactData);
        if (!fingerprint) return null;
        const contacts = await db.contacts.toArray();
        return contacts.find((contact) => {
            if (!contact || contact.id === excludeId) return false;
            return buildContactDuplicateFingerprint(contact) === fingerprint;
        }) || null;
    }

    function buildRelationFingerprint(relation) {
        if (!relation) return '';
        const source = String(relation.sourceGlobalId || '').trim();
        const target = String(relation.targetGlobalId || '').trim();
        if (!source || !target) return '';
        const relationType = normalizeContactIdentityText(relation.relationType);
        const relationDesc = normalizeContactIdentityText(relation.relationDesc);
        return [source, target, relationType, relationDesc].join('|');
    }

    async function restoreImportedRelations(importedRelations, oldGlobalId, newGlobalId) {
        if (!Array.isArray(importedRelations) || !importedRelations.length || !newGlobalId) return;
        const existingRelations = await db.relations.toArray();
        const seen = new Set(existingRelations.map((relation) => buildRelationFingerprint(relation)).filter(Boolean));
        for (const rawRelation of importedRelations) {
            if (!rawRelation || typeof rawRelation !== 'object') continue;
            const relation = { ...rawRelation };
            delete relation.id;
            if (oldGlobalId && relation.sourceGlobalId === oldGlobalId) relation.sourceGlobalId = newGlobalId;
            if (oldGlobalId && relation.targetGlobalId === oldGlobalId) relation.targetGlobalId = newGlobalId;
            const key = buildRelationFingerprint(relation);
            if (!key || seen.has(key)) continue;
            await db.relations.add(relation);
            seen.add(key);
        }
    }

