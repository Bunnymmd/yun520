    function mountDropdownToLayer(list, wrap) {
        if (!list || !wrap) return;
        if (!list.__miniDropdownHome) {
            list.__miniDropdownHome = {
                parent: list.parentNode,
                nextSibling: list.nextSibling
            };
        }
        list.__miniDropdownAnchor = wrap;
        const layer = getDropdownLayer();
        if (list.parentNode !== layer) layer.appendChild(list);
    }

    function restoreDropdownMount(list) {
        if (!list || !list.__miniDropdownHome) return;
        const { parent, nextSibling } = list.__miniDropdownHome;
        if (!parent) return;
        if (nextSibling && nextSibling.parentNode === parent) parent.insertBefore(list, nextSibling);
        else parent.appendChild(list);
    }

    function positionDropdown(list) {
        if (!list) return;
        const wrap = getDropdownAnchor(list);
        if (!wrap) return;

        mountDropdownToLayer(list, wrap);

        const gap = 6;
        const rect = wrap.getBoundingClientRect();
        const viewportHeight = window.innerHeight || document.documentElement.clientHeight || 0;
        const naturalWidth = Math.max(1, wrap.offsetWidth || rect.width || 1);
        const scale = Math.max(0.01, rect.width / naturalWidth);

        list.style.display = 'flex';
        list.style.position = 'absolute';
        list.style.left = `${rect.left}px`;
        list.style.right = 'auto';
        list.style.top = '0';
        list.style.bottom = 'auto';
        list.style.width = `${naturalWidth}px`;
        list.style.marginTop = '0';
        list.style.marginBottom = '0';
        list.style.visibility = 'hidden';
        list.style.pointerEvents = 'none';
        list.style.transform = `scale(${scale})`;
        list.style.transformOrigin = 'left top';
        list.style.zIndex = '10021';

        const naturalHeight = Math.max(
            44,
            Math.ceil((list.scrollHeight || list.offsetHeight || 0) + 2)
        );
        const availableBelow = Math.max(96, Math.floor(viewportHeight - rect.bottom - gap));
        const availableAbove = Math.max(96, Math.floor(rect.top - gap));
        const displayHeight = naturalHeight * scale;
        const openUpward = displayHeight > availableBelow && availableAbove > availableBelow;
        const maxHeight = Math.max(96, Math.floor((openUpward ? availableAbove : availableBelow) / scale));

        list.dataset.direction = openUpward ? 'up' : 'down';
        list.style.top = openUpward ? 'auto' : `${rect.bottom + gap}px`;
        list.style.bottom = openUpward ? `${Math.max(0, viewportHeight - rect.top + gap)}px` : 'auto';
        list.style.maxHeight = `${maxHeight}px`;
        list.style.transformOrigin = openUpward ? 'left bottom' : 'left top';
        list.style.visibility = '';
        list.style.pointerEvents = 'auto';
        wrap.classList.add('is-open');
        activeDropdownList = list;
    }

