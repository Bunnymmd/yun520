            const otherId = isSource ? rel.targetGlobalId : rel.sourceGlobalId;
            const otherEntity = allEntities.find(e => e.globalId === otherId);
            
            if(!otherEntity) return; 
            
            const avatarStyle = otherEntity.avatar ? `style="background-image: url('${otherEntity.avatar}')"` : '';
